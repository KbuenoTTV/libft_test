int					ft_isspace(int c)
{
	unsigned char	val;

	val = (unsigned char)c;
	if (val == '\t' || val == '\n' || val == '\v'
		|| val == '\f' || val == '\r' || val == ' ')
		return (1);
	return (0);
}
